				new InputSystem(),
				new TriggerSystem(),
				new StateSystem(),
				new BehaviourSystem()
			),
			EntityFactory.createCamera(
				Vec3(320, 180, 0),
				640, 360, 0.1f, 100.0f
			)
		)
		val startButton = resourceManager.getPrefab("startButton", Vec3(320, 240, 0))
		val cursor = resourceManager.getPrefab("cursor", Vec3(320, 180, 0))
		scene.addEntity(startButton)
		scene.addEntity(cursor)

		scene
	}

